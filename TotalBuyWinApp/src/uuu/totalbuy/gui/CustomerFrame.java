    private void getButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getButtonActionPerformed
        // TODO add your handling code here:
        System.out.println("getButtonActionPerformed");
        try {
            String id = (String) idField.getValue();
            if (id == null) {
                throw new TotalBuyException("查詢前請輸入id!");
            }
            Customer c = service.get(id);
            if (c != null) {
                //將c顯示在各controls中
                showCustomer(c);
            } else {
                JOptionPane.showMessageDialog(this, "查無客戶: " + id);
                showCustomer(new Customer());
            }
        } catch (Exception ex) {
            //將ex顯示在訊息對話窗中
            showException(ex);
        }
    }//GEN-LAST:event_getButtonActionPerformed

    private void getAllButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_getAllButtonActionPerformed
        // TODO add your handling code here:
        try {
            List<Customer> list = service.getAll();

            //將list顯示在customersTable中
            DefaultTableModel model = (DefaultTableModel) customersTable1.getModel();
            model.setRowCount(0);
            if (list != null && list.size() > 0) {
                for (Customer c : list) {
                    model.addRow(
                            new Object[]{
                                c.getId(),
                                c.getName(),
                                String.valueOf(c.getGender()),
                                c.getEmail(),
                                c.getBirthday(),
                                c.getPhone(),
                                c.getAddress(),
                                c.isMarried(),
                                c.getBloodType()
                            }
                    );
                }
            }
        } catch (Exception ex) {
            showException(ex);
        }
    }//GEN-LAST:event_getAllButtonActionPerformed


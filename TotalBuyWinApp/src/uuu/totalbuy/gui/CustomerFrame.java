    private Customer getCustomer() throws TotalBuyException {
        Customer c = null;
        if (vipCheck.isSelected()) {
            c = new VIP();
        } else {
            c = new Customer();
        }
        try {
            c.setId((String) idField.getValue());
            c.setName(nameField.getText());
            c.setPassword(String.valueOf(passwordField.getPassword()));
            c.setGender(femaleRadioButton.isSelected() ? Customer.FEMALE : Customer.MALE);
            c.setEmail(emailField.getText());
            c.setBirthday((Date) birthdayField.getValue());
            c.setAddress(addressField.getText());
            c.setPhone(phoneField.getText());
            c.setMarried(marriedCheck.isSelected());
            c.setBloodType((BloodType) bloodTypeCombo.getSelectedItem());
            if (c instanceof VIP) {
                Number n = (Number) discountSpinner.getValue();
                ((VIP) c).setDiscount(n.intValue());
            }
        } catch (TotalBuyException ex) {
            throw ex;
        }
        return c;
    }

    private void showCustomer(Customer c) {
        System.out.println("查詢完成: " + c);
        idField.setValue(c.getId());
        nameField.setText(c.getName());
        maleRadioButton.setSelected(c.getGender() == Customer.MALE);
        femaleRadioButton.setSelected(c.getGender() == Customer.FEMALE);
        passwordField.setText(c.getPassword());
        emailField.setText(c.getEmail());

        birthdayField.setValue(c.getBirthday());
        phoneField.setText(c.getPhone());
        addressField.setText(c.getAddress());
        bloodTypeCombo.setSelectedItem(c.getBloodType());
        marriedCheck.setSelected(c.isMarried());

        vipCheck.setSelected(c instanceof VIP);
        if (c instanceof VIP) {
            discountSpinner.setValue(((VIP) c).getDiscount());
        } else {
            discountSpinner.setValue(0);
            discountSpinner.setEnabled(false);
        }
    }

    private void showException(Exception ex) {
        JOptionPane.showMessageDialog(this,
                ex instanceof TotalBuyException ? ex.getMessage() : ex.toString(), "系統發生錯誤", JOptionPane.ERROR_MESSAGE);
    }

